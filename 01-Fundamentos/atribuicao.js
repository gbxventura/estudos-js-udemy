const a = 7; // atribuicao
let b = 3;

// b = b + a
b += a; // junto com a atribuição, está acrecentando.
console.log(b); // 10

// b = b -4
b -= 4;
console.log(b); // 6

// b = b * 2
b *= 2;
console.log(b); // 12

// b = b / 2
b /= 2;
console.log(b); // 6

// b = b % 2
b %= 2;
console.log(b); // 0
